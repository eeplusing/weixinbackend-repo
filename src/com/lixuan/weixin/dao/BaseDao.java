public interface BaseDao<T>
{
	/********************************************************************************
     * 添加实体
     * @param entity
     ********************************************************************************/
    void save(T entity);

    /********************************************************************************
     * 保存或修改实体  
     * @param entity
     ********************************************************************************/
	 void saveOrUpdateEntity(T t);
	
    /********************************************************************************
     * 删除实体
     * 
     * @param id
     ********************************************************************************/
    void delete(Long id) throws Exception;
    
    /********************************************************************************
     * 根据提供的id数组删除符合条件的实体
     * 
     * @param id
     * @throws Exception 
     ********************************************************************************/
    void delete(Long[] ids) throws Exception;

    /********************************************************************************
     * 更新实体
     * 
     * @param entity
     ********************************************************************************/
    void update(T entity);
    
    void batchEntityByHql(String hql, Object... objects);

    /********************************************************************************
     * 按id查询
     * 
     * @param id
     * @return
     ********************************************************************************/
    T getById(Long id);

    /********************************************************************************
     * 按id查询
     * 
     * @param ids
     * @return
     ********************************************************************************/
    List<T> getByIds(Long[] ids);

    /********************************************************************************
     * 查询所有
     * 
     * @return
     ********************************************************************************/
    List<T> findAll();

    /********************************************************************************
     * 根据提供的查询参数，查询符合条件的对象集合
     * @param queryHelper:HQL语句与参数列表
     * @return
     ********************************************************************************/
    List<T> getByQueryHelper(QueryHelper queryHelper);
    
    /********************************************************************************
     * 根据提供hql语句和对应位置上的查询参数，
     * @param hql, 筛选参数
     * @return
     ********************************************************************************/
    List<T> findEntityByHql(String hql, Object... objects);
    
    /********************************************************************************
     * 公共的查询分页信息的方法
     * @param pageNum:当前页号码
     * @param pageSize:每页数据量
     * @param queryHelper:HQL语句与参数列表
     * @return
     ********************************************************************************/
    PageBean getPageBean(int pageNum, int pageSize, QueryHelper queryHelper);
}